   public void executeSQlQuery(String query, String message)
   {
       com.sun.jdi.connect.spi.Connection con = getConnection();
       com.mysql.cj.xdevapi.Statement st;
       try{
           st = con.createStatement();
           if((st.executeUpdate(query)) == 1)
           {
               // refresh jtable data
               DefaultTableModel model = (DefaultTableModel)jTable_Display_Users.getModel();
               model.setRowCount(0);
               Show_ttable();
               
               JOptionPane.showMessageDialog(null, "Trainer added"+message+" Succefully");
           }else{
               JOptionPane.showMessageDialog(null, "Insufficient data "+message);
           }
       }catch(Exception ex){
           ex.printStackTrace();
       }
   }
    
 
