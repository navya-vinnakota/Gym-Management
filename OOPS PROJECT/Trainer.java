     public ArrayList<Trainer> getTrainer_detailsList()
   {
       ArrayList<Trainer> usersList = new ArrayList<Trainer>();
       com.sun.jdi.connect.spi.Connection connection = getConnection();
       
       String query = "SELECT * FROM  `trainer_details` ";
       com.mysql.cj.xdevapi.Statement st;
       ResultSet rs;
       
       try {
           st = connection.createStatement();
           rs = st.executeQuery(query);
           Trainer trainer_details;
           while(rs.next())
           {
               trainer_details  = new  trainer_details(rs.getInt("id"),rs.getString("fname"),rs.getString("lname"),rs.getInt("age"));
               usersList.add( trainer_details);
           }
       } catch (Exception e) {
           e.printStackTrace();
       }
       return usersList;
   }
   
   // Display Data In JTable
   
   public void Show_ttable()
   {
       ArrayList<Trainer> list = getTrainer_detailsList();
       DefaultTableModel model = (DefaultTableModel)ttable.getModel();
       Object[] row = new Object[4];
       for(int i = 0; i < list.size(); i++)
       {
           row[0] = list.get(i).getName();
           row[1] = list.get(i).getAge();
           row[2] = list.get(i).getAddress();
           row[3] = list.get(i).getMobile_no();
           
           model.addRow(row);
       }
    }
        
